export const formatDate = (inputDate) => {
  const months = [
    "января",
    "февраля",
    "марта",
    "апреля",
    "мая",
    "июня",
    "июля",
    "августа",
    "сентября",
    "октября",
    "ноября",
    "декабря",
  ];
  const date = new Date(inputDate);
  return `${date.getDate()} ${months[date.getMonth()]} ${date.getFullYear()}`;
};

